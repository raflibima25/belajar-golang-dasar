package main

import "fmt"

func main() {
	var name string

	name = "Rafli Bima"
	fmt.Println(name)

	name = "Rafli Pratandra"
	fmt.Println(name)

	var friendName = "Budi"
	fmt.Println(friendName)

	var age = 20
	fmt.Println(age)

	country := "Indonesia"
	fmt.Println(country)

	var (
		firstName = "Rafli"
		lastName  = "Bima"
	)

	fmt.Println(firstName)
	fmt.Println(lastName)
}
